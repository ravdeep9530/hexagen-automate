function FeatureCard({ feature, index }: { feature: Feature; index: number }): JSX.Element {
  const ref = useRef<HTMLDivElement>(null);
  const isInView = useInView(ref, { once: true, margin: '-100px' });

  return (
    <motion.div
      ref={ref}
      initial={{ opacity: 0, y: 20 }}
      animate={isInView ? { opacity: 1, y: 0 } : { opacity: 0, y: 20 }}
      transition={{ duration: 0.5, delay: index * 0.1 }}
      className="group relative rounded-2xl border border-slate-200 bg-white p-6 transition-shadow hover:shadow-lg"
    >
      <div className="mb-4 inline-flex rounded-lg bg-blue-50 p-3 text-blue-600 transition-colors group-hover:bg-blue-600 group-hover:text-white">
        {feature.icon}
      </div>
      <h3 className="mb-2 text-lg font-semibold text-slate-900">{feature.title}</h3>
      <p className="text-sm leading-relaxed text-slate-600">{feature.description}</p>
    </motion.div>
  );
}

